    img_w = data["img_w"]
    img_h = data["img_h"]

    img_bgr = cv2.imread(data["img_path"])
    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    ax.imshow(img_rgb)

    # Zone overlays
