"""---
## Section 9 — Compute Accuracy, Precision, Recall, F1-score

Evaluation uses IoU >= 0.50 to classify detections as TP, FP, or FN.  
Computed over the **validation set** using ground-truth YOLO labels.

```
Accuracy  = TP / (TP + FP + FN)
Precision = TP / (TP + FP)
Recall    = TP / (TP + FN)
F1-score  = 2 * P * R / (P + R)
```
"""

def xywhn_to_xyxy(box, img_w, img_h):
    """Convert YOLO normalised cx cy w h to absolute x1 y1 x2 y2."""
    cx, cy, w, h = box
    x1 = (cx - w / 2) * img_w
    y1 = (cy - h / 2) * img_h
    x2 = (cx + w / 2) * img_w
    y2 = (cy + h / 2) * img_h
    return np.array([x1, y1, x2, y2])


def compute_iou(boxA, boxB):
    """Compute IoU between two boxes in xyxy format."""
    xA = max(boxA[0], boxB[0])
    yA = max(boxA[1], boxB[1])
    xB = min(boxA[2], boxB[2])
    yB = min(boxA[3], boxB[3])
    inter = max(0, xB - xA) * max(0, yB - yA)
    areaA = (boxA[2] - boxA[0]) * (boxA[3] - boxA[1])
    areaB = (boxB[2] - boxB[0]) * (boxB[3] - boxB[1])
    union = areaA + areaB - inter
    return inter / union if union > 0 else 0.0


def load_gt_boxes(label_path, img_w, img_h):
    """Load ground-truth boxes from a YOLO .txt label file."""
    boxes = []
    if not Path(label_path).exists():
        return boxes
    with open(label_path) as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) < 5:
                continue
            cls = int(parts[0])
            xyxy = xywhn_to_xyxy([float(x) for x in parts[1:5]], img_w, img_h)
            boxes.append({"cls": cls, "box": xyxy})
    return boxes


def evaluate_detections(img_paths, label_dir, model, iou_thresh=0.5, conf_thresh=0.25):
    """
    Run inference and match predictions against ground-truth using IoU threshold.
    Returns TP, FP, FN counts and per-image prediction records.
    """
    TP_total = FP_total = FN_total = 0
    all_preds = {}   # image_stem -> list of pred dicts

    label_dir = Path(label_dir)

    for img_path in img_paths:
        img_path = Path(img_path)
        stem = img_path.stem

        img = cv2.imread(str(img_path))
        if img is None:
            continue
        h, w = img.shape[:2]

        # Ground truth
        lbl_file = label_dir / (stem + ".txt")
        gt_boxes = load_gt_boxes(lbl_file, w, h)

        # Predictions
        results = model.predict(
            source=str(img_path),
            conf=conf_thresh,
            device=DEVICE,
            verbose=False,
        )
        pred_boxes = []
        if results and results[0].boxes is not None:
            for box in results[0].boxes:
                xyxy = box.xyxy[0].cpu().numpy()
                cls  = int(box.cls[0].item())
                conf = float(box.conf[0].item())
                pred_boxes.append({"cls": cls, "box": xyxy, "conf": conf})

        all_preds[stem] = pred_boxes

        # Match preds to GT
        matched_gt = set()
        matched_pred = set()

        for pi, pred in enumerate(pred_boxes):
            best_iou  = 0.0
            best_gi   = -1
            for gi, gt in enumerate(gt_boxes):
                if gi in matched_gt:
                    continue
                iou = compute_iou(pred["box"], gt["box"])
                if iou > best_iou:
                    best_iou = iou
                    best_gi  = gi
            if best_iou >= iou_thresh and best_gi >= 0:
                TP_total   += 1
                matched_gt.add(best_gi)
                matched_pred.add(pi)
            else:
                FP_total += 1

        FN_total += len(gt_boxes) - len(matched_gt)

    return TP_total, FP_total, FN_total, all_preds


